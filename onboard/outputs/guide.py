"""Markdown onboarding guide generator.

Produces a comprehensive Markdown guide from scan results,
architecture maps, git analysis, and AI summaries.
"""

from __future__ import annotations

from datetime import datetime
from pathlib import Path

from ..scanner import ScanResult
from ..analyzer import ArchitectureMap
from ..git_analyzer import GitAnalysis
from ..engine import AISummary


def generate_guide_markdown(
    scan: ScanResult,
    arch: ArchitectureMap,
    git: GitAnalysis | None,
    ai: AISummary | None,
) -> str:
    """Generate the full onboarding guide as Markdown."""
    sections: list[str] = []

    # Header
    repo_name = scan.root.name
    sections.append(f"# Onboarding Guide: {repo_name}\n")
    sections.append(f"*Generated by [Onboard](https://github.com/Jaxz714/onboard) on {datetime.now().strftime('%Y-%m-%d %H:%M')}*\n")
    sections.append("---\n")

    # Table of contents
    toc = [
        "## Table of Contents\n",
        "1. [Overview](#overview)",
        "2. [Quick Start](#quick-start)",
        "3. [Architecture](#architecture)",
        "4. [Project Structure](#project-structure)",
        "5. [Dependencies](#dependencies)",
        "6. [Modules](#modules)",
        "7. [Entry Points](#entry-points)",
        "8. [Testing](#testing)",
        "9. [Git Activity](#git-activity)",
        "10. [Where to Start](#where-to-start)",
    ]
    sections.append("\n".join(toc) + "\n")
    sections.append("---\n")

    # 1. Overview
    sections.append("## Overview\n")
    if ai and ai.overview:
        sections.append(ai.overview + "\n")
    else:
        sections.append(_generate_overview_text(scan, arch) + "\n")

    # 2. Quick Start
    sections.append("## Quick Start\n")
    sections.append(_generate_quick_start(scan, arch) + "\n")

    # 3. Architecture
    sections.append("## Architecture\n")
    if ai and ai.architecture_notes:
        sections.append(ai.architecture_notes + "\n")
    sections.append(_generate_architecture_section(arch) + "\n")

    # 4. Project Structure
    sections.append("## Project Structure\n")
    sections.append(_generate_structure_tree(scan) + "\n")

    # 5. Dependencies
    sections.append("## Dependencies\n")
    sections.append(_generate_dependencies_section(scan) + "\n")

    # 6. Modules
    sections.append("## Modules\n")
    sections.append(_generate_modules_section(arch, ai) + "\n")

    # 7. Entry Points
    sections.append("## Entry Points\n")
    sections.append(_generate_entry_points_section(scan) + "\n")

    # 8. Testing
    sections.append("## Testing\n")
    sections.append(_generate_testing_section(scan) + "\n")

    # 9. Git Activity
    if git:
        sections.append("## Git Activity\n")
        sections.append(_generate_git_section(git) + "\n")

    # 10. Where to Start
    sections.append("## Where to Start\n")
    if ai and ai.getting_started:
        sections.append(ai.getting_started + "\n")
    else:
        sections.append(_generate_where_to_start(scan, arch) + "\n")

    return "\n".join(sections)


